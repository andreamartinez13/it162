<p><b>Client:</b> Glambeauty</p>
<p><b>Industry:</b> Beauty & Cosmetics</p>
<p><b>Product/Service:</b> Sourcing and selling U.S. makeup to customers in Mexico.</p>
<p><b>Key Result:</b> Building trust through quality products and personal connections in the local market.</p>

<p>Glambeauty is a small, family-run business founded by Natalia, a teenager from Mexico, with help from her mom. The business focuses on bringing makeup from the U.S. to their hometown. Unlike large companies, Natalia’s mom personally selects and buys products to ensure they are high quality and in line with the latest trends.</p>

<p><b>Problem:</b></p>
<p>In Natalia’s hometown, makeup lovers struggle to find authentic U.S. brands. They worry about fakes and high shipping costs when ordering online. Many don’t trust unknown sellers or websites, leaving them without the products they love.</p>

<p><b>Solution:</b></p>
<p>Glambeauty solves this by:</p>
<ul>
	<li>Quality Assurance: Natalia’s mom travels to the U.S. to personally review, buy, and bring back makeup.</li>
	<li>Local Sales: Products are sold in person, allowing customers to see and trust what they are buying.</li>
	<li>Affordable Pricing: No big vendor or middleman means fair prices for customers.</li>
</ul>

<p><b>Results</b></p>
<ul>
    <li>Trust Built: Customers feel confident buying directly from someone in their community.</li>
	<li>Personal Service: 90% of first customers said they loved the personal touch.</li>
	<li>Positive Feedback: Customers praised the authentic products and fair prices.</li>
	<li>Ready to Expand: If successful in the hometown, Glambeauty plans to grow into new areas.</li>
</ul>

<p><b>Conclusion</b></p>
<p>Glambeauty is changing how people buy makeup by focusing on trust and quality.  Natalia and her mom are helping others feel beautiful with makeup they can rely on, and it is accesible to all.</p>






